    /**
     * Converts a pixel value into ems
     * 
     * @param baseSize double The base size in pixels
     * @param desiredSize double The desired size, also in pixels
     * @return The converted size in ems
     */
    @Override
    public double px_to_em(double baseSize, double desiredSize) {
        double result;
        
        result = desiredSize / baseSize;
        
        return result;
    }
    
    /**
     * Converts a pixel value into a percent.
     * 
     * @param baseSize double The base size in pixels
     * @param desiredSize double The desired size, also in pixels
     * @return The converted size as a percent
     * @see MainFrame#px_to_em(double, double)
     */
    @Override
    public double px_to_percent(double baseSize, double desiredSize) {
        double result;
        
        result = (desiredSize / baseSize) * 100;
        
        return result;
    }

