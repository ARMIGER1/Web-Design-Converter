    private void btn_convertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_convertActionPerformed
        
        // Make sure that both fields are filled and consist ONLY of numbers and maybe a decimal.
        if (tf_base_size.getText().matches(REGEX) && tf_desired_size.getText().matches(REGEX) && (!"".equals(tf_base_size.getText()) && !"".equals(tf_desired_size.getText()) ) ) {
            readyFlag = true;
        } else {
            readyFlag = false;
        }
        
        if (readyFlag == true) {
            double base = Double.parseDouble(tf_base_size.getText());
            double target = Double.parseDouble(tf_desired_size.getText());
            double answer = 0;
            
            switch (cb_unit.getSelectedIndex()) {
                case UNIT_EM:
                    answer = px_to_em(base, target);
                    answer = Double.parseDouble(threeDigits.format(answer));
                    break;
                case UNIT_PERCENT:
                    answer = px_to_percent(base, target);
                    answer = Double.parseDouble(oneDigit.format(answer));
                    break;
            }
            
            tf_result.setText(Double.toString(answer));
            
        } else {
            //JOptionPane.showConfirmDialog(null, "Please enter a number for your base size and desired size.", "Error!", WIDTH);
            JOptionPane.showConfirmDialog(null, "Please enter a number for your base and desired sizes.", "Error!", JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_convertActionPerformed

